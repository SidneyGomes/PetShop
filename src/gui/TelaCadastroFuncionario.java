    private void carregaTabela() {
        DefaultTableModel padrao = new DefaultTableModel(new Object[]{"Nome", "Codigo"}, 0);
        for (int i = 0; i < pess.getArrayList().size(); i++) {
            if (pess.getArrayList().get(i) instanceof Funcionarios) {
                padrao.addRow(new Object[]{pess.getArrayList().get(i).getNome(), pess.getArrayList().get(i).getCodigo()});
            }
        }

        jTable1.setModel(padrao);

    }

