    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField1.getText().equals("") == false && jTextField2.getText().equals("") == false) {
                try {
                    pess.procurarFuncionario(Integer.parseInt(jTextField2.getText()));
                    JOptionPane.showMessageDialog(null, "Codigo jÃ¡ cadastrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jTextField2.setText("");

                } catch (FuncionarioNaoEncontradoException ex) {
                    Funcionarios funcionarios = new Funcionarios(jTextField1.getText(), Integer.parseInt(jTextField2.getText()));
                    pess.cadastrar(funcionarios);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    JOptionPane.showMessageDialog(null, "Funcionario cadastrado !", "Ok", JOptionPane.INFORMATION_MESSAGE);
                    salvar.salvarArquivo(pess);

                    carregaTabela();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campo(s) nao preenchido(s)", "Erro", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Campo(s) digitado(s) incorretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        System.out.println(posicaoSelecionada);

        try {

            pess.editarFuncionario(posicaoSelecionada, jTextField1.getText(), Integer.valueOf(jTextField2.getText()));

            JOptionPane.showMessageDialog(null, "Funcionario Editado !", "Ok", JOptionPane.INFORMATION_MESSAGE);
            salvar.salvarArquivo(pess);
            jTextField1.setText("");
            jTextField2.setText("");

            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            carregaTabela();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Campo(s) digitado(s) incorretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");

            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);

        ArrayList<Funcionarios> arrayTabela = new ArrayList<>();

        for (int i = 0; i < pess.getArrayList().size(); i++) {
            if (pess.getArrayList().get(i) instanceof Funcionarios) {
                arrayTabela.add((Funcionarios) pess.getArrayList().get(i));
            }
        }

        this.posicaoSelecionada = jTable1.getSelectedRow();

        if (this.posicaoSelecionada >= 0 && this.posicaoSelecionada < arrayTabela.size()) {
            jTextField1.setText(arrayTabela.get(this.posicaoSelecionada).getNome());
            jTextField2.setText(String.valueOf(arrayTabela.get(this.posicaoSelecionada).getCodigo()));
        }

        try {
            posicaoSelecionada = pess.procurarFuncionario(Integer.valueOf(jTextField2.getText()));
        } catch (FuncionarioNaoEncontradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTable1MouseClicked

