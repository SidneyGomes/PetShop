    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);

        ArrayList<Servicos> serv = new ArrayList<Servicos>();

        for (int i = 0; i < prest.getArrayPrestabilidade().size(); i++) {
            if (prest.getArrayPrestabilidade().get(i) instanceof Servicos) {
                serv.add((Servicos) prest.getArrayPrestabilidade().get(i));
            }
        }

        posicaoSelecionada = jTable1.getSelectedRow();

        if (posicaoSelecionada >= 0 && posicaoSelecionada < serv.size()) {
            jTextField5.setText(serv.get(posicaoSelecionada).getDescricao());
            jTextField6.setText(String.valueOf(serv.get(posicaoSelecionada).getCodigo()));
            jTextField8.setText(String.valueOf(serv.get(posicaoSelecionada).getPreco()));
        }

        try {
            posicaoSelecionada = prest.procurarServicos(Integer.valueOf(jTextField6.getText()));
        } catch (ServicoNaoEncontradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jTable1MouseClicked

