    private void carregarTabela() {

        DefaultTableModel padrao = new DefaultTableModel(new Object[]{"Descrição", "Codigo", "Valor"}, 0);
        for (int i = 0; i < prest.getArrayPrestabilidade().size(); i++) {
            if (prest.getArrayPrestabilidade().get(i) instanceof Servicos) {
                padrao.addRow(new Object[]{prest.getArrayPrestabilidade().get(i).getDescricao(), prest.getArrayPrestabilidade().get(i).getCodigo(), prest.getArrayPrestabilidade().get(i).getPreco()});
            }
        }
        jTable1.setModel(padrao);
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField6.getText().equals("") == false && jTextField5.getText().equals("") == false && jTextField8.getText().equals("") == false) {
                try {
                    prest.procurarServicos(Integer.valueOf(jTextField6.getText()));
                    JOptionPane.showMessageDialog(null, "Servico já cadastrado.", "Erro", JOptionPane.ERROR_MESSAGE);

                } catch (ServicoNaoEncontradoException ex) {
                    Servicos serv = new Servicos(Integer.valueOf(jTextField6.getText()), jTextField5.getText(), Double.valueOf(jTextField8.getText()));
                    prest.cadastrar(serv);
                    salvar.salvarArquivo(prest);
                    carregarTabela();
                    JOptionPane.showMessageDialog(null, "Servico Cadastrado.", "Ok", JOptionPane.INFORMATION_MESSAGE);
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField8.setText("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campo(s) nao preenchido(s).", "Erro", JOptionPane.ERROR_MESSAGE);

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Campo(s) digitado(s) incorretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField6.getText().equals("") == false && jTextField5.getText().equals("") == false && jTextField8.getText().equals("") == false) {
                try {
                    prest.procurarServicos(Integer.valueOf(jTextField6.getText()));

                    prest.editar(posicaoSelecionada, jTextField5.getText(), Integer.valueOf(jTextField6.getText()), Double.valueOf(jTextField8.getText()));
                    salvar.salvarArquivo(prest);
                    JOptionPane.showMessageDialog(null, "Servico editado.", "Ok", JOptionPane.INFORMATION_MESSAGE);
                    carregarTabela();
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField8.setText("");
                    botoesIniciais();

                } catch (ServicoNaoEncontradoException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campo(s) nao preenchido(s).", "Erro", JOptionPane.ERROR_MESSAGE);

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Campo(s) digitado(s) incorretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

