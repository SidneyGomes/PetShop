    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            ArrayList<Clientes> cli = new ArrayList<Clientes>();

            for (int i = 0; i < novaVenda.pessoas.getArrayList().size(); i++) {
                if (novaVenda.pessoas.getArrayList().get(i) instanceof Clientes) {
                    cli.add((Clientes) novaVenda.pessoas.getArrayList().get(i));
                }
            }

            CartaoCredito cartaoCredito = new CartaoCredito(Double.valueOf(novaVenda.jLabel2.getText()),
                    Long.valueOf(jTextField1.getText()), jComboBox1.getSelectedIndex() + 1, Integer.valueOf(jFormattedTextField1.getText()), jFormattedTextField2.getText());

            int procuraFuncionario = cadastroPessoas.procurarFuncionario(Integer.valueOf(TelaNovaVenda.func.jLabel6.getText()));

            Relatorio rel = new Relatorio(new Date(),
                    cli.get(novaVenda.pegaCliente()),
                    (Funcionarios) cadastroPessoas.getArrayList().get(procuraFuncionario),
                    novaVenda.visualizarVenda.getArrayPrestabilidade(),
                    cartaoCredito);

            cadastroRelatorios.cadastrar(rel);
            salvar.salvarArquivo(cadastroRelatorios);

            caixa.setBalancoAtual(caixa.getBalancoAtual() + Double.valueOf(jLabel2.getText()));
            caixa.setBalancoTotal(caixa.getBalancoTotal() + Double.valueOf(jLabel2.getText()));
            salvar.salvarArquivo(caixa);

            JOptionPane.showMessageDialog(null, "Pagamento efetuado.", "Ok", JOptionPane.INFORMATION_MESSAGE);
            novaVenda.botoesIniciais();
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Campo(s) digitado(s) incorretamente", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

