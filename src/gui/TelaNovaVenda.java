    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        this.carregaTabelaProdutos();
        this.carregarListaPacotes();
        jButton1.setEnabled(true);

        ArrayList<Servicos> servicos = new ArrayList<>();

        for (int i = 0; i < prest.getArrayPrestabilidade().size(); i++) {
            if (prest.getArrayPrestabilidade().get(i) instanceof Servicos) {
                servicos.add((Servicos) prest.getArrayPrestabilidade().get(i));
            }
        }

        posicaoSelecionada = jTable1.getSelectedRow();
        try {
            posicaoSelecionada = prest.procurarServicos(servicos.get(posicaoSelecionada).getCodigo());
        } catch (ServicoNaoEncontradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:

        this.carregaTabelaProdutos();
        this.carregaTabelaServicos();
        jButton1.setEnabled(true);

        jComboBox3.removeAllItems();

        ArrayList<Pacotes> pacotes = new ArrayList<>();

        for (int i = 0; i < prest.getArrayPrestabilidade().size(); i++) {
            if (prest.getArrayPrestabilidade().get(i) instanceof Pacotes) {
                pacotes.add((Pacotes) prest.getArrayPrestabilidade().get(i));
            }
        }

        posicaoSelecionada = jTable2.getSelectedRow();

        if (this.posicaoSelecionada >= 0 && this.posicaoSelecionada < pacotes.size()) {
            try {
                posicaoSelecionada = prest.procurarPacotes(pacotes.get(posicaoSelecionada).getCodigo());
            } catch (PacoteNaoEncontradoException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }

        for (int i = 0; i < ((Pacotes) prest.getArrayPrestabilidade().get(posicaoSelecionada)).getArray().size(); i++) {
            jComboBox3.addItem(((Pacotes) prest.getArrayPrestabilidade().get(posicaoSelecionada)).getArray().get(i).getDescricao());
        }

    }//GEN-LAST:event_jTable2MouseClicked

