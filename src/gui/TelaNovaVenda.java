    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        jComboBox2.removeAllItems();
        ArrayList<Clientes> clientes = new ArrayList<Clientes>();

        for (int i = 0; i < pessoas.getArrayList().size(); i++) {
            if (pessoas.getArrayList().get(i) instanceof Clientes) {
                clientes.add((Clientes) pessoas.getArrayList().get(i));
            }
        }

        posicaoSelecionada = jComboBox1.getSelectedIndex();
        try {
            posicaoSelecionada = pessoas.procurarCliente(clientes.get(posicaoSelecionada).getCodigo());
        } catch (ClienteNaoEncontradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

        for (int i = 0; i < ((Clientes) pessoas.getArrayList().get(posicaoSelecionada)).getAnimaiszinho().size(); i++) {
            jComboBox2.addItem(((Clientes) pessoas.getArrayList().get(posicaoSelecionada)).getAnimaiszinho().get(i).getNome());
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            jButton2.setEnabled(true);
            visualizarVenda.cadastrar(prest.getArrayPrestabilidade().get(posicaoSelecionada));
            Double valor = prest.getArrayPrestabilidade().get(posicaoSelecionada).getPreco() + Double.valueOf(jLabel2.getText());

            this.carregaTabelaServicos();
            this.carregarListaPacotes();
            this.carregaTabelaProdutos();
            posicaoSelecionada = -1;
            jComboBox3.removeAllItems();
            jLabel2.setText("");
            jLabel2.setText(String.valueOf(valor));

            this.carregarTabelaVenda();

            JOptionPane.showMessageDialog(null, "Adicionado.", "Ok", JOptionPane.INFORMATION_MESSAGE);
        } catch (ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "Nenhuma opção selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        double valor = Double.valueOf(jLabel2.getText()) - visualizarVenda.getArrayPrestabilidade().get(posicaoSelecionada).getPreco();
        jLabel2.setText(String.valueOf(valor));
        this.visualizarVenda.remover(posicaoSelecionada);

        this.carregarTabelaVenda();
        jButton4.setEnabled(false);
        jButton1.setEnabled(true);
        if (this.visualizarVenda.getArrayPrestabilidade().size() <= 0) {
            jButton2.setEnabled(false);
        }

        JOptionPane.showMessageDialog(null, "Removido.", "Ok", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton4ActionPerformed

