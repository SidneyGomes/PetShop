    public void troco() {
        new Thread() {
            public void run() {
                while (true) {
                    try {
                        if (Double.valueOf(jTextField1.getText()) - Double.valueOf(jLabel4.getText()) > 0) {
                            jLabel6.setText(String.valueOf(Double.valueOf(jTextField1.getText()) - Double.valueOf(jLabel4.getText())));
                        }
                    } catch (NumberFormatException ex) {
                        jLabel6.setText("0.0");
                    }
                }
            }
        }.start();
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (Double.parseDouble(jTextField1.getText()) >= Double.parseDouble(jLabel4.getText())) {
            if (Double.valueOf(jLabel6.getText()) < caixa.getValorCaixa()) {

                ArrayList<Clientes> cli = new ArrayList<Clientes>();

                for (int i = 0; i < novaVenda.pessoas.getArrayList().size(); i++) {
                    if (novaVenda.pessoas.getArrayList().get(i) instanceof Clientes) {
                        cli.add((Clientes) novaVenda.pessoas.getArrayList().get(i));
                    }
                }

                Dinheiro dinheiro = new Dinheiro(Double.valueOf(jLabel4.getText()), Double.valueOf(jLabel6.getText()));

                int procuraFuncionario = 0;

                try {
                    procuraFuncionario = cadastroPessoas.procurarFuncionario(Integer.valueOf(TelaNovaVenda.func.jLabel6.getText()));
                } catch (FuncionarioNaoEncontradoException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }

                Relatorio rel = new Relatorio(new Date(),
                        cli.get(novaVenda.pegaCliente()),
                        (Funcionarios) cadastroPessoas.getArrayList().get(procuraFuncionario),
                        novaVenda.visualizarVenda.getArrayPrestabilidade(),
                        dinheiro);

                cadastroRelatorios.cadastrar(rel);
                salvar.salvarArquivo(cadastroRelatorios);

                caixa.setBalancoAtual(caixa.getBalancoAtual() + Double.valueOf(jLabel4.getText()));
                caixa.setBalancoTotal(caixa.getBalancoTotal() + Double.valueOf(jLabel4.getText()));
                caixa.setValorCaixa(caixa.getValorCaixa() + Double.valueOf(jLabel4.getText()));
                salvar.salvarArquivo(caixa);

                JOptionPane.showMessageDialog(null, "Pagamento efetuado.", "Ok", JOptionPane.INFORMATION_MESSAGE);
                novaVenda.botoesIniciais();
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Nao existe dinheiro em Caixa para troco, contate administrador", "Ok", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nao Ã© possivel realizar pagamento", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

